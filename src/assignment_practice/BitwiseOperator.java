package assignment_practice;

public class BitwiseOperator {
	public static void main(String[] args) {
		
//		AND-  & (all conditions should be TRUE then only true else false)
//		TRUE  & TRUE = TRUE
//		TRUE  & FALSE = FALSE
//		FALSE & TRUE = FALSE
//		FALSE & FALSE = FALSE
		
	
//		OR- | (any condition should be TRUE then true else false)
//		TRUE  | TRUE  = TRUE
//		TRUE  | FALSE = TRUE
//		FALSE | TRUE  = TRUE
//		FALSE | FALSE = FALSE
		System.out.println(true & false);
		
//		Logical Operator- &&  ||
		
		System.out.println(false || false);
	}

}
